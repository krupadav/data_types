print('Numeric type')
print('19 is', type(19))
print('19.15 is', type(19.15))
print('19+0j is', type(19 + 0j))
print('Sequence type')
print('[12, 15, 16]', type([12, 15, 16]))
print('(12,15,20)', type((12, 15, 20)))
print('range(5)', type(range(5)))
print('Mapping type')
print('{1:5}', type({1: 5}))
print('Set type')
print('{1, 2, 3}', type({1, 2, 3}))
print('frozenset(1, 2, 3)', type(frozenset((1, 2, 3))))
print('String')
print('hello', type('hello'))